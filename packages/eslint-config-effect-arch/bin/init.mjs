#!/usr/bin/env node
// effect-arch-init — wire the effect-arch enforcement presets into a repo.
//
// This is deterministic file surgery (a slash command is not). It never
// splices your existing ESLint config (unreliable); it writes importable
// fragments and prints the two lines to add. Everything it touches is recorded
// in .claude/effect-arch.json so `eject` reverses it exactly.
//
//   effect-arch-init                 # dry-run: print the plan
//   effect-arch-init --write         # apply
//   effect-arch-init --write --link /path/to/effect-arch-kit
//                                    # use file: deps (before the presets are on npm)
//   effect-arch-init eject           # undo everything init wrote
//
// Flags: --cwd <dir>, --runtime-file <path>, --link <path>, --write, --yes

import { readFileSync, writeFileSync, existsSync, mkdirSync, rmSync } from 'node:fs'
import { join, dirname } from 'node:path'

const PRESET_VERSION = '0.1.0'
const SENTINEL = '.claude/effect-arch.json'
const ESLINT_FRAGMENT = 'eslint.config.effect-arch.mjs'
const DEPCRUISE_FRAGMENT = '.dependency-cruiser.effect-arch.cjs'
const PRESET_DEPS = [
  'eslint-config-effect-arch',
  'eslint-plugin-effect-arch',
  'dependency-cruiser-effect-arch',
  'dependency-cruiser',
]

function parseArgs(argv) {
  const args = { _: [], write: false, yes: false, cwd: process.cwd() }
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i]
    if (a === '--write') args.write = true
    else if (a === '--yes' || a === '-y') args.yes = true
    else if (a === '--cwd') args.cwd = argv[++i]
    else if (a === '--runtime-file') args.runtimeFile = argv[++i]
    else if (a === '--link') args.link = argv[++i]
    else args._.push(a)
  }
  return args
}

function detectLayout(cwd) {
  const has = (p) => existsSync(join(cwd, p))
  const candidates = [
    'src/app/RuntimeContext.tsx',
    'src/app/runtime.tsx',
    'src/app/runtime.ts',
  ]
  const runtimeFile = candidates.find(has) || 'src/app/RuntimeContext.tsx'
  return {
    hasDomain: has('src/domain'),
    hasApplication: has('src/application'),
    hasReact: has('src/react'),
    runtimeFile,
  }
}

function depSpec(link) {
  if (link) {
    return {
      'eslint-config-effect-arch': `file:${join(link, 'packages/eslint-config-effect-arch')}`,
      'eslint-plugin-effect-arch': `file:${join(link, 'packages/eslint-plugin-effect-arch')}`,
      'dependency-cruiser-effect-arch': `file:${join(link, 'packages/dependency-cruiser-effect-arch')}`,
      'dependency-cruiser': '^16',
    }
  }
  return {
    'eslint-config-effect-arch': `^${PRESET_VERSION}`,
    'eslint-plugin-effect-arch': `^${PRESET_VERSION}`,
    'dependency-cruiser-effect-arch': `^${PRESET_VERSION}`,
    'dependency-cruiser': '^16',
  }
}

const eslintFragment = (runtimeFile) => `// Generated by effect-arch-init. Do not edit by hand — re-run init to regenerate.
// Add these two lines to your eslint.config.(mjs|js):
//   import effectArchConfig from './${ESLINT_FRAGMENT}'
//   export default [ ...yourConfig, ...effectArchConfig ]
import { effectArch } from 'eslint-config-effect-arch'

export default effectArch({ runtimeFile: '${runtimeFile}' })
`

const depcruiseFragment = () => `// Generated by effect-arch-init. Run: npx depcruise --config ${DEPCRUISE_FRAGMENT} src
const { effectArchDepcruise } = require('dependency-cruiser-effect-arch')

module.exports = effectArchDepcruise()
`

function loadJson(p) {
  return JSON.parse(readFileSync(p, 'utf8'))
}
function writeJson(p, obj) {
  writeFileSync(p, JSON.stringify(obj, null, 2) + '\n')
}

function run() {
  const args = parseArgs(process.argv.slice(2))
  const cwd = args.cwd
  const sentinelPath = join(cwd, SENTINEL)

  if (args._[0] === 'eject') return eject(cwd, sentinelPath)

  const pkgPath = join(cwd, 'package.json')
  if (!existsSync(pkgPath)) {
    console.error(`No package.json at ${cwd}. Pass --cwd <repo-root>.`)
    process.exit(1)
  }
  const layout = detectLayout(cwd)
  const runtimeFile = args.runtimeFile || layout.runtimeFile
  const deps = depSpec(args.link)

  console.log('effect-arch-init plan')
  console.log('  cwd:          ' + cwd)
  console.log('  runtime file: ' + runtimeFile + (existsSync(join(cwd, runtimeFile)) ? '' : '  (not found — adjust with --runtime-file)'))
  console.log('  layout:       ' + ['domain', 'application', 'react'].filter((k) => layout['has' + k[0].toUpperCase() + k.slice(1)]).join(', ') || '(none detected)')
  console.log('  deps:         ' + (args.link ? 'file: links → ' + args.link : 'npm ^' + PRESET_VERSION))
  console.log('  will write:   ' + [SENTINEL, ESLINT_FRAGMENT, DEPCRUISE_FRAGMENT].join(', '))
  console.log('  will patch:   package.json (devDependencies + `depcruise` script)')

  if (!args.write) {
    console.log('\nDry run. Re-run with --write to apply, then `npm install`.')
    console.log('After install, add to your eslint config:')
    console.log(`  import effectArchConfig from './${ESLINT_FRAGMENT}'`)
    console.log('  export default [ ...yourConfig, ...effectArchConfig ]')
    return
  }

  // 1. Fragments
  mkdirSync(dirname(sentinelPath), { recursive: true })
  writeFileSync(join(cwd, ESLINT_FRAGMENT), eslintFragment(runtimeFile))
  writeFileSync(join(cwd, DEPCRUISE_FRAGMENT), depcruiseFragment())

  // 2. package.json — track exactly what we add for a clean eject.
  const pkg = loadJson(pkgPath)
  pkg.devDependencies ||= {}
  pkg.scripts ||= {}
  const addedDeps = []
  for (const [name, spec] of Object.entries(deps)) {
    if (!pkg.devDependencies[name] && !(pkg.dependencies && pkg.dependencies[name])) {
      pkg.devDependencies[name] = spec
      addedDeps.push(name)
    }
  }
  const addedScripts = []
  if (!pkg.scripts.depcruise) {
    pkg.scripts.depcruise = `depcruise --config ${DEPCRUISE_FRAGMENT} src`
    addedScripts.push('depcruise')
  }
  writeJson(pkgPath, pkg)

  // 3. Sentinel — records everything for eject + lets skills/hook detect the kit.
  writeJson(sentinelPath, {
    version: PRESET_VERSION,
    installedBy: 'effect-arch-init',
    runtimeFile,
    files: [ESLINT_FRAGMENT, DEPCRUISE_FRAGMENT],
    packageJson: { addedDeps, addedScripts },
  })

  console.log('\nWrote fragments + sentinel and patched package.json.')
  console.log('Next:')
  console.log('  1. npm install')
  console.log(`  2. add to eslint.config: import effectArchConfig from './${ESLINT_FRAGMENT}'; export default [ ...yourConfig, ...effectArchConfig ]`)
  console.log('  3. npx eslint src   # rules are live')
  console.log('\nRollback anytime: effect-arch-init eject')
}

function eject(cwd, sentinelPath) {
  if (!existsSync(sentinelPath)) {
    console.error('Nothing to eject: no .claude/effect-arch.json sentinel found.')
    process.exit(1)
  }
  const record = loadJson(sentinelPath)
  for (const f of record.files || []) {
    const p = join(cwd, f)
    if (existsSync(p)) {
      rmSync(p)
      console.log('removed ' + f)
    }
  }
  const pkgPath = join(cwd, 'package.json')
  if (existsSync(pkgPath)) {
    const pkg = loadJson(pkgPath)
    for (const name of record.packageJson?.addedDeps || []) {
      if (pkg.devDependencies) delete pkg.devDependencies[name]
    }
    for (const s of record.packageJson?.addedScripts || []) {
      if (pkg.scripts) delete pkg.scripts[s]
    }
    writeJson(pkgPath, pkg)
    console.log('reverted package.json additions')
  }
  rmSync(sentinelPath)
  console.log('removed ' + SENTINEL)
  console.log('\nEjected. Run `npm install` to drop the uninstalled devDependencies.')
  console.log('You still need to remove the two import lines from your eslint config by hand.')
}

run()
